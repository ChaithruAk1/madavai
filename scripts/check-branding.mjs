#!/usr/bin/env node
/**
 * Madav branding scanner.
 *
 * Enforces the "built by and for Madav" policy (see MADAV.md):
 *  - HARD-FORBIDDEN identity / competitor / legacy-brand references => CI fails.
 *  - PROVIDER names are legitimate only inside the provider/integration layer
 *    (they are how the product actually calls external services). Anywhere else
 *    they are reported for review.
 *
 * Zero dependencies. Run: `node scripts/check-branding.mjs`
 * Writes a full report to docs/branding/REFERENCES-REPORT.md and exits non-zero
 * if any hard-forbidden reference is present.
 */
import { readdirSync, statSync, readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { join, extname, relative } from 'node:path';

const ROOT = process.cwd();
const SKIP_DIRS = new Set([
  'node_modules', '.git', 'dist', 'dist-test', 'build', 'release', '.checkpoints',
  '.playwright-mcp', '_legacy', '_planning', 'coverage', '.next', 'vendor',
]);
const SCAN_EXT = new Set([
  '.js', '.jsx', '.ts', '.tsx', '.cjs', '.mjs', '.json', '.md', '.html', '.css', '.txt', '.yml', '.yaml', '.svelte', '.vue',
]);

// Hard-forbidden: brand/identity that must never appear in a Madav codebase.
const FORBIDDEN = [
  { re: /open[\s-]?web\s?ui/i, label: 'Open WebUI (competitor branding)' },
  { re: /brain[\s-]?edge/i, label: 'BrainEdge (legacy product name -> rebrand to Madav)' },
  { re: /chatgpt/i, label: 'ChatGPT (competitor branding)' },
  { re: /claude[\s-]?code/i, label: 'Claude Code (external product name)' },
];

// Provider names: allowed in the provider/integration layer, reported elsewhere.
const PROVIDERS = [
  /\banthropic\b/i, /\bclaude\b/i, /\bopenai\b/i, /\bollama\b/i,
  /\bgemini\b/i, /\bgrok\b/i, /\bdeepseek\b/i, /\bmistral\b/i,
];
const PROVIDER_LAYER =
  /(providers?\/|integration|model[-_]?router|models\/|connectors?\/|adapters?\/|provider-)/i;

function walk(dir, out = []) {
  let entries;
  try { entries = readdirSync(dir); } catch { return out; }
  for (const name of entries) {
    if (SKIP_DIRS.has(name)) continue;
    const p = join(dir, name);
    let s; try { s = statSync(p); } catch { continue; }
    if (s.isDirectory()) walk(p, out);
    else if (SCAN_EXT.has(extname(name).toLowerCase())) out.push(p);
  }
  return out;
}

const SELF_EXEMPT = (rel) =>
  rel === 'MADAV.md' ||
  rel === 'Chat1.md' ||  // untracked dev conversation log
  rel.startsWith('scripts/check-branding') ||
  rel.startsWith('docs/branding/') ||
  rel.startsWith('docs/blueprint/') ||      // the blueprint legitimately names what Madav integrates
  rel.startsWith('REBUILD-STATE');

const files = walk(ROOT);
const forbidden = [];
const providerOutsideLayer = [];
let providerInLayer = 0;

for (const f of files) {
  const rel = relative(ROOT, f).replace(/\\/g, '/');
  if (SELF_EXEMPT(rel)) continue;
  let text; try { text = readFileSync(f, 'utf8'); } catch { continue; }
  const inLayer = PROVIDER_LAYER.test(rel);
  text.split(/\r?\n/).forEach((line, i) => {
    const scanLine = line.replace(/ClaudeCodeUI/gi, 'RepoDir'); // the install folder name is a filesystem path, not branding
    for (const { re, label } of FORBIDDEN) {
      if (re.test(scanLine)) forbidden.push({ rel, ln: i + 1, label, text: line.trim().slice(0, 160) });
    }
    for (const re of PROVIDERS) {
      if (re.test(line)) {
        if (inLayer) providerInLayer++;
        else providerOutsideLayer.push({ rel, ln: i + 1, text: line.trim().slice(0, 160) });
      }
    }
  });
}

// ---- write report ----
mkdirSync(join(ROOT, 'docs', 'branding'), { recursive: true });
const byFile = (arr) => {
  const m = new Map();
  for (const h of arr) m.set(h.rel, (m.get(h.rel) || 0) + 1);
  return [...m.entries()].sort((a, b) => b[1] - a[1]);
};
const lines = [];
lines.push('# Branding reference report', '');
lines.push(`Generated by \`scripts/check-branding.mjs\` on ${new Date().toISOString()}.`, '');
lines.push(`- Files scanned: **${files.length}**`);
lines.push(`- HARD-FORBIDDEN references: **${forbidden.length}** (must be 0 to pass)`);
lines.push(`- Provider mentions inside the provider layer (OK): **${providerInLayer}**`);
lines.push(`- Provider mentions OUTSIDE the provider layer (review): **${providerOutsideLayer.length}**`, '');
lines.push('## Hard-forbidden references (fix these)', '');
if (!forbidden.length) lines.push('_None. 🎉_', '');
else for (const h of forbidden.slice(0, 500)) lines.push(`- \`${h.rel}:${h.ln}\` — ${h.label}: \`${h.text}\``);
lines.push('', '## Provider mentions outside the provider layer (relocate or rebrand)', '');
lines.push('These are legitimate only when isolated in the provider/integration layer. Top files:', '');
for (const [file, n] of byFile(providerOutsideLayer).slice(0, 60)) lines.push(`- \`${file}\` — ${n}`);
writeFileSync(join(ROOT, 'docs', 'branding', 'REFERENCES-REPORT.md'), lines.join('\n') + '\n');

// ---- console summary + exit code ----
console.log(`[branding] scanned ${files.length} files`);
console.log(`[branding] hard-forbidden: ${forbidden.length}`);
console.log(`[branding] provider mentions: ${providerInLayer} in-layer (ok), ${providerOutsideLayer.length} outside (review)`);
console.log('[branding] report -> docs/branding/REFERENCES-REPORT.md');
if (forbidden.length) {
  console.error(`\n[branding] FAIL: ${forbidden.length} hard-forbidden reference(s). See report.`);
  process.exit(1);
}
console.log('[branding] PASS');
